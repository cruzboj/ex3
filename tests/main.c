#define HANDLE_RESULT(cmd) \
    do { \
        HrmResult res = (cmd); \
        if (res != HRM_SUCCESS) { \
            ex3Tester_PrintfError(res); \
        } \
    } while (0)

static const char * hrm_results_strings[] = {
    "HRM_SUCCESS",
    "HRM_OUT_OF_MEMORY",
    "HRM_NULL_ARGUMENT",
    "HRM_INVALID_WORKER_ID",
    "HRM_INVALID_WAGE",
    "HRM_INVALID_NUM_OF_SHIFTS",
    "HRM_WORKER_ALREADY_EXISTS",
    "HRM_WORKER_DOES_NOT_EXIST",
    "HRM_SHIFT_ALREADY_EXISTS",
    "HRM_SHIFT_DOES_NOT_EXIST",
    "HRM_SHIFTS_OVERFLLOW",
    "HRM_NO_WORKERS",
    "HRM_NO_SHIFTS",
};

static const char *hrm_result_to_string(HrmResult result)
{
    if (result < HRM_SUCCESS || result > HRM_NO_SHIFTS)
    {
        return "Unknown result";
    }
    return hrm_results_strings[result];
}
static void ex3Tester_PrintfError(HrmResult res)
{
    fprintf(stderr, "%s\n", hrm_result_to_string(res));
}

